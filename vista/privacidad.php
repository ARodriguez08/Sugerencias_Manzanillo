                        <li>Procesar sus solicitudes y sugerencias</li>
                        <li>Enviarle correos electrónicos</li>
                        <li>Encontrar y prevenir fraudes</li>
                    </ul>
                    
                    <h2 class="h4 mt-4">3. Cookies y Tecnologías de Seguimiento</h2>
                    <p>Utilizamos cookies y tecnologías de seguimiento similares para rastrear la actividad en nuestra plataforma y almacenar cierta información. Las cookies son archivos con una pequeña cantidad de datos que pueden incluir un identificador único anónimo. Las cookies se envían a su navegador desde un sitio web y se almacenan en su dispositivo.</p>
                    
                    <p>También utilizamos balizas web, etiquetas y scripts para recopilar y rastrear información y para mejorar y analizar nuestra plataforma.</p>
                    
                    <p>Puede instruir a su navegador para que rechace todas las cookies o para que le avise cuando se envía una cookie. Sin embargo, si no acepta cookies, es posible que no pueda utilizar algunas partes de nuestra plataforma.</p>
                    
                    <h2 class="h4 mt-4">4. Compartir su Información Personal</h2>
                    <p>No vendemos, intercambiamos ni transferimos de otro modo a terceros su información de identificación personal. Esto no incluye terceros de confianza que nos ayudan a operar nuestra plataforma, siempre que esas partes acuerden mantener esta información confidencial.</p>
                    
                    <p>Podemos compartir su información en las siguientes situaciones:</p>
                    <ul>
                        <li><strong>Con funcionarios públicos:</strong> Para procesar y dar seguimiento a sus solicitudes y sugerencias.</li>
                        <li><strong>Con proveedores de servicios:</strong> Podemos compartir su información con proveedores de servicios de terceros para realizar tareas en nuestro nombre y para ayudarnos a proporcionar nuestros servicios.</li>
                        <li><strong>Para transferencias comerciales:</strong> Podemos compartir o transferir su información en relación con, o durante las negociaciones de, cualquier fusión, venta de activos de la empresa, financiamiento o adquisición de todo o una parte de nuestro negocio a otra empresa.</li>
                        <li><strong>Con su consentimiento:</strong> Podemos divulgar su información personal para cualquier otro propósito con su consentimiento.</li>
                        <li><strong>Con las autoridades públicas:</strong> Si estamos obligados por ley a hacerlo o en respuesta a solicitudes legales válidas.</li>
                    </ul>
                    
                    <h2 class="h4 mt-4">5. Retención de Datos</h2>
                    <p>Retendremos su información personal solo durante el tiempo que sea necesario para los propósitos establecidos en esta Política de Privacidad. Mantendremos y utilizaremos su información en la medida necesaria para cumplir con nuestras obligaciones legales, resolver disputas y hacer cumplir nuestros acuerdos legales y políticas.</p>
                    
                    <p>También retendremos los datos de uso para fines de análisis interno. Los datos de uso generalmente se retienen por un período más corto, excepto cuando estos datos se utilizan para fortalecer la seguridad o mejorar la funcionalidad de nuestra plataforma, o cuando estamos legalmente obligados a retener estos datos por períodos más largos.</p>
                    
                    <h2 class="h4 mt-4">6. Seguridad de sus Datos</h2>
                    <p>La seguridad de sus datos es importante para nosotros, pero recuerde que ningún método de transmisión por Internet o método de almacenamiento electrónico es 100% seguro. Si bien nos esforzamos por utilizar medios comercialmente aceptables para proteger su información personal, no podemos garantizar su seguridad absoluta.</p>
                    
                    <h2 class="h4 mt-4">7. Sus Derechos de Protección de Datos</h2>
                    <p>Usted tiene ciertos derechos de protección de datos. En cualquier momento, usted tiene derecho a:</p>
                    <ul>
                        <li><strong>Acceder y recibir una copia de su información personal</strong> que tenemos sobre usted.</li>
                        <li><strong>Rectificar</strong> cualquier información personal que tengamos sobre usted que sea inexacta o incompleta.</li>
                        <li><strong>Solicitar la eliminación</strong> de su información personal que mantenemos.</li>
                        <li><strong>Restringir el procesamiento</strong> de su información personal.</li>
                        <li><strong>Solicitar la transferencia</strong> de su información personal a usted o a un tercero.</li>
                        <li><strong>Retirar su consentimiento</strong> en cualquier momento cuando dependemos de su consentimiento para procesar su información personal.</li>
                    </ul>
                    
